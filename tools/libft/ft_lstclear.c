#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list *elem;

	if (!*lst)
		return ;
	while (*lst)
	{
		elem = (*lst)->next;
		ft_lstdelone(*lst, (*del));
		*lst = elem;
	}
	*lst = NULL;
}
